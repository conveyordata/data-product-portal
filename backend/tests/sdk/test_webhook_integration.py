# 1. Create a Spy Handler using your generated SDK base class
class WebhookSpy(EmptyEventHandler):
    def __init__(self):
        self.captured_payload: Optional[DataProductCreatedEvent] = None

    # This method name matches the convention generated by your SDK script
    async def on_data_product_created(
        self, data: DataProductCreatedEvent
    ) -> dict[str, str]:
        self.captured_payload = data
        return {"status": "success"}


class TestSDKWebhookIntegration:
    def test_sdk_handler_receives_and_parses_generated_cloudevent(
        self, client, payload, user_with_create_data_product_rights, monkeypatch
    ):
        spy = WebhookSpy()

        monkeypatch.setattr(
            settings, "WEBHOOK_V2_URL", "http://sdk-receiver.local/webhook"
        )

        async def intercept_webhook_delivery(event_type: str, data: dict) -> None:
            import uuid
            from datetime import datetime, timezone

            cloudevent_envelope = {
                "specversion": "1.0",
                "id": str(uuid.uuid4()),
                "source": "data-product-portal",
                "type": event_type,
                "time": datetime.now(timezone.utc).isoformat(),
                "data": data,
            }

            async def mock_receive():
                return {
                    "type": "http.request",
                    "body": json.dumps(cloudevent_envelope).encode(),
                    "more_body": False,
                }

            nested_request = Request(
                scope={
                    "type": "http",
                    "method": "POST",
                    "path": "/webhook",
                    "headers": Headers().raw,
                },
                receive=mock_receive,
            )

            await spy.dispatch_routing(nested_request)

        mock_call = AsyncMock(side_effect=intercept_webhook_delivery)
        patch("app.core.webhooks.v2.call_v2_webhook", new=mock_call).start()

        response = client.post(ENDPOINT, json=payload)
        assert response.status_code == 200
        assert spy.captured_payload is not None
        assert spy.captured_payload.data_product.name == payload["name"]
        assert spy.captured_payload.data_product.description == payload["description"]

        patch.stopall()

    @pytest.mark.asyncio
    async def test_sdk_handler_gracefully_ignores_unrecognized_event_types(self):
        handler = EmptyEventHandler()

        rogue_envelope = {
            "specversion": "1.0",
            "id": "12345",
            "source": "data-product-portal",
            "type": "galaxy.destroyed",  # Unrecognized type
            "time": "2026-06-08T00:00:00Z",
            "data": {"extinction_level": "high"},
        }

        async def mock_receive():
            return {
                "type": "http.request",
                "body": json.dumps(rogue_envelope).encode(),
                "more_body": False,
            }

        nested_request = Request(
            scope={
                "type": "http",
                "method": "POST",
                "path": "/webhook",
                "headers": Headers().raw,
            },
            receive=mock_receive,
        )

        result = await handler.dispatch_routing(nested_request)
        assert result["status"] == "ignored"
        assert "galaxy.destroyed" in result["reason"]
