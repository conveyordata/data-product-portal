"""Generate docs/docs/developer-guide/webhook-events.md by introspecting the FastAPI app.

Usage:
    python backend/app/webhook_doc_export.py

Run this after modifying v2 event emissions in any router, then commit the output.
"""

from __future__ import annotations

import sys
from pathlib import Path

# Allow running as a standalone script from the repo root.
sys.path.insert(0, str(Path(__file__).parent.parent))

from typing import TYPE_CHECKING

from fastapi.routing import APIRoute

if TYPE_CHECKING:
    from fastapi import FastAPI

REPO_ROOT = Path(__file__).parent.parent.parent
DOCS_OUTPUT = REPO_ROOT / "docs/docs/developer-guide/webhook-events.md"
API_DOCS_BASE = "/docs/api/#schema"

GROUPS: dict[str, str] = {
    "data_product": "Data Product Events",
    "output_port": "Output Port Events",
    "technical_asset": "Technical Asset Events",
}

HEADER = """\
---
title: Webhook Events (v2)
description: Reference for all v2 webhook events emitted by the Data Product Portal
sidebar_position: 9
---

<!-- Auto-generated by backend/app/webhook_doc_export.py — do not edit manually -->

The Data Product Portal emits webhook events following the [CloudEvents](https://cloudevents.io/) specification.
All events are delivered as HTTP POST requests to the configured endpoint.

## Configuration

Set the `WEBHOOK_V2_URL` environment variable to receive events.
When not set, no events are emitted.

## Envelope

Every event is wrapped in a CloudEvents envelope:

```json
{
  "specversion": "1.0",
  "id": "<uuid>",
  "source": "data-product-portal",
  "type": "data_product.created",
  "time": "2026-04-18T12:00:00Z",
  "data": { ... }
}
```

## Events

"""


def build_event_list(app: FastAPI) -> list[dict]:
    """Walk app routes and collect events from webhook-emitting dependencies."""
    events: list[dict] = []
    seen: set[tuple] = set()

    for route in app.routes:
        if not isinstance(route, APIRoute):
            continue
        for dep in route.dependencies:
            fn = dep.dependency
            if not hasattr(fn, "_webhook_event_type"):
                continue
            event_type: str = fn._webhook_event_type
            event_model = fn._webhook_payload_model
            method = sorted(route.methods)[0]
            key = (event_type, method, route.path)
            if key in seen:
                continue
            seen.add(key)
            payload = {
                name: field.annotation.__name__
                for name, field in event_model.model_fields.items()
            }
            events.append(
                {
                    "event": event_type,
                    "method": method,
                    "path": route.path,
                    "payload": payload,
                }
            )

    events.sort(key=lambda e: e["event"])
    return events


def render_payload(payload: dict[str, str]) -> str:
    """Render payload field names as Markdown links to the OpenAPI schema."""
    if not payload:
        return "\u2014"
    return ", ".join(
        f"[`{key}`]({API_DOCS_BASE}/{cls})" for key, cls in payload.items()
    )


def generate_markdown(events: list[dict]) -> str:
    """Render the full Markdown document."""
    lines = [HEADER]

    groups: dict[str, list[dict]] = {}
    for event in events:
        prefix = event["event"].split(".")[0]
        groups.setdefault(prefix, []).append(event)

    ordered_prefixes = list(GROUPS.keys()) + [p for p in groups if p not in GROUPS]

    for prefix in ordered_prefixes:
        if prefix not in groups:
            continue
        title = GROUPS.get(prefix, f"{prefix.replace('_', ' ').title()} Events")
        lines.append(f"### {title}\n")
        lines.append("| Event | HTTP Trigger | Payload |")
        lines.append("|-------|-------------|---------|")
        for event in groups[prefix]:
            payload_str = render_payload(event["payload"])
            lines.append(
                f"| `{event['event']}` "
                f"| `{event['method']} {event['path']}` "
                f"| {payload_str} |"
            )
        lines.append("")

    return "\n".join(lines)


def main() -> None:
    from app.main import app  # imported here to avoid side-effects at module level

    events = build_event_list(app)
    if not events:
        sys.exit(1)

    markdown = generate_markdown(events)
    DOCS_OUTPUT.write_text(markdown)


if __name__ == "__main__":
    main()
